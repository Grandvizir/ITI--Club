<h2 class="offset1">Acc&egrave;s membre du BDE</h2>
	<div class="bs-docs-candidat">
		<p><blockquote>
		
			<p>Les BDE sont pr&eacute;sents dans les universit&eacute;s et dans les &eacute;coles. Ils fonctionnent par fili&egrave;res ou pour tout un &eacute;tablissement. Leur r&ocirc;le est particuli&egrave;rement important dans les grandes &eacute;coles. En effet, c'est le BDE qui organise toute la vie festive de l'&eacute;cole comme les soir&eacute;es, les grands repas ou qui est un point de rencontre entre les diff&eacute;rentes associations de l'&eacute;cole. L'association est en g&eacute;n&eacute;ral &eacute;lue tous les ans par les &eacute;tudiants de l'&eacute;cole ainsi que par les &eacute;tudiants &eacute;trangers en &eacute;change dans certaines &eacute;coles.</p>
			<p> Cette &eacute;lection s'effectue &agrave; la suite d'une campagne qui se d&eacute;roule sur une ou plusieurs semaines voyant s'affronter plusieurs listes. Cette campagne voit la plupart du temps se d&eacute;rouler des &eacute;v&eacute;nements exceptionnels en termes de soir&eacute;es ou d'animation de campus.</p>
			<p>Les BDE s'affirment de mani&egrave;re globale apolitiques. Cependant, ils n'ont pas tous la m&ecirc;me lecture du concept. Une partie consid&egrave;re qu'&ecirc;tre apolitique implique le fait de ne pas participer aux &eacute;lections universitaires puisqu'il s'agit, une fois &eacute;lu, de prendre des d&eacute;cisions politiques, tandis que d'autre participent ou prennent parti, int&egrave;grent des listes revendiquant ce positionnement d'apolitisme.</p>
			<p> Les BDE des &eacute;coles d'ing&eacute;nieurs sont par exemple tous membres du <acronym title="Bureau National des Eleves Ingenieurs">BNEI</acronym> lui m&ecirc;me membre de l'organisation Promotion et D&eacute;fense des Etudiants et vote donc en sa faveur aux &eacute;lections du <acronym title="Conseil national de l'enseignement superieur et de la recherche">CNESER</acronym>.
L&#146;universit&eacute; ou l&#146;&eacute;cole concern&eacute;e fournit parfois l'essentiel du budget de fonctionnement. De nombreux BDE compl&egrave;tent leur budget &agrave; l'aide d'autofinancement (soir&eacute;es, vente de gadgets &agrave; l&#146;effigie de l'&eacute;tablissement ou du BDE...). Certains choisissent de recourir aux sponsors ou &agrave; des partenaires commerciaux, tandis que d'autres s'y refusent pour des questions &eacute;thiques.</p>
			
			
